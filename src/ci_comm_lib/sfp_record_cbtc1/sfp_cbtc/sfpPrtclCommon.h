/******************************************************************************************
* 功能描述      : 返回当前时刻的函数
*                 针对不同的平台进行不同的处理
*                 Windows平台：通过调用函数GetCurTime()来获取当前时刻
*                 VxWorks平台：平台提供当前时钟值变量的指针
* 输入参数      : struct Unify_Info *pUnifyInfo   平台统一结构体
* 输入输出参数  : 
* 输出参数      :
* 全局变量      :
* 返回值        : 当前时刻        
*******************************************************************************************/

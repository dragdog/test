/******************************************************************************************
* 功能描述      : 将设备TYPE和设备ID转换成long型的设备名
* 输入参数      : UINT8 devType 设备类型
*                 UINT8 devID  设备ID
* 输入输出参数  : 
* 输出参数      :
* 全局变量      :
* 返回值        : unsigned INT32 型的设备名        
*******************************************************************************************/
